#include "shmup.h"

void	fill(t_point p1, t_point p2, char c)
{
	int	x;
	int	y;

	y = p1.y;
	while (y <= p2.y)
	{
		x = p1.x;
		while (x <= p2.x)
		{
			draw(x, y, c);
			x++;
		}
		y++;
	}
}

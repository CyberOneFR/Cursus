#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	const char	*base = "9876543210123456789";
	char		tmp[12];
	size_t		i;

	i = 12;
	tmp[--i] = 0;
	while (n != 0 || i == 11)
	{
		tmp[--i] = base[(n % 10) + 9];
		if (n < 0 && n > -10)
			tmp[--i] = '-';
		n /= 10;
	}
	write(fd, &tmp[i], 11 - i);
}
